square = lambda x: x * x
result = square(5)
print(f"The result of the lambda function is: {result}")
# Output: The result of the lambda function is: 25

# A lambda function is a small anonymous function.
# It can take any number of arguments, but can only have one expression.    
# Lambda functions are often used for short, throwaway functions, especially as arguments to higher-order functions.
# They are syntactically restricted to a single expression.