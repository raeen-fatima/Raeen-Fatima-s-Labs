# python programe to demonstrate boolean data type
# boolean data type is a data type that can have one of two possible values: True or False
# boolean data type is used to represent the truth value of an expression
print(type(True))  # Output: <class 'bool'>
print(type(False))  # Output: <class 'bool'>
print(1 == 1)  # Output: True
print(1 == 2)  # Output: False
print(1 < 2)  # Output: True
print(1 > 2)  # Output: False
